#Faça um Programa que peça as 4 notas bimestrais e mostre a média.

nota1 = float(input("Digite a nota do 1 Bimetre: "))
nota2 = float(input("Digite a nota do 2 Bimetre: "))
nota3 = float(input("Digite a nota do 3 Bimetre: "))
nota4 = float(input("Digite a nota do 4 Bimetre: "))

media = (nota1 + nota2 + nota3 + nota4) / 4

print("A média das notas é", media)